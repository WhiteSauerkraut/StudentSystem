public class Department {
    private String department_no;
    private String department_name;
    private String department_addr;
    private String department_tel;
    private String department_dec;

    public String getDepartment_no() {
        return department_no;
    }

    public void setDepartment_no(String department_no) {
        this.department_no = department_no;
    }

    public String getDepartment_name() {
        return department_name;
    }

    public void setDepartment_name(String department_name) {
        this.department_name = department_name;
    }

    public String getDepartment_addr() {
        return department_addr;
    }

    public void setDepartment_addr(String department_addr) {
        this.department_addr = department_addr;
    }

    public String getDepartment_tel() {
        return department_tel;
    }

    public void setDepartment_tel(String department_tel) {
        this.department_tel = department_tel;
    }

    public String getDepartment_dec() {
        return department_dec;
    }

    public void setDepartment_dec(String department_dec) {
        this.department_dec = department_dec;
    }
}
